namespace Calculation
{
    public class Operations
    {
        public static int Add(int sum1, int sum2)
        {
            return sum1 + sum2;
        }
        
        public static int Minus(int min1, int min2)
        { 
            return min1 - min2; 
        }

        public static int Multiply(int mul1, int mul2)
        {
            return mul1 * mul2;
        }

        public static int Divide(int div1, int div2)
        {
            return div1 / div2;
        }
    }
}